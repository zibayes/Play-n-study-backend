let showResults = document.getElementById("show_results");
let showLeaders = document.getElementById("show_leaders");
let showGraphics = document.getElementById("show_graphics");
let showFriends = document.getElementById("show_friends");
let results = document.getElementById("test-results");
let leaders = document.getElementById("leaders-table");
let graphics = document.getElementById("progress-graphics");
let friends = document.getElementById("subscribers-table");
showResults.addEventListener("click", function(e) {
    showResults.setAttribute("disabled", "disabled");
    showLeaders.removeAttribute("disabled");
    showGraphics.removeAttribute("disabled");
    showFriends.removeAttribute("disabled");
    $("#leaders-table").fadeOut(400);
    $("#progress-graphics").fadeOut(400);
    $("#subscribers-table").fadeOut(400);
    leaders.setAttribute("hidden", "hidden");
    graphics.setAttribute("hidden", "hidden");
    friends.setAttribute("hidden", "hidden");
    results.removeAttribute("hidden");
    $("#test-results").fadeIn(400);
});
